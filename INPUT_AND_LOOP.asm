.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'ENTER A CHARACTER:$'
.CODE


MAIN PROC
MOV AX, @DATA
MOV DS, AX

MOV AH, 9  

LEA DX, MSG    
INT 21H

; INPUT

MOV AH, 1
INT 21H
MOV BL, AL


MOV AH, 2
MOV DL, 10D ; 0AH -> NEW LINE ASCII CODE
INT 21H
MOV DL, 13D
INT 21H

; LOOP
MOV CX, 20D   

L:
; OUTPUT
MOV AH, 2
LEA DL, BL
INT 21H

; FOR LINEBREAK
MOV AH, 2
MOV DL, 10D ; 0AH -> NEW LINE ASCII CODE
INT 21H
MOV DL, 13D
INT 21H


LOOP L

MAIN ENDP
END MAIN