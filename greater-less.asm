.MODEL SMALL
.STACK 100H

.DATA
PROMPT DB 'INPUT NUMBER: $'
MSG1 DB 'NUMBER IS GREATER THAN 5!$'
MSG2 DB 'NUMBER IS LESS THAN 5!$'

.CODE

MAIN PROC

MOV AX, @DATA
MOV DS, AX


MOV AH, 9 
LEA DX, PROMPT
INT 21H

MOV AH, 1
INT 21H



CMP AL, 053D
JG L1

JL L2


L1:

LEA DX, MSG1
MOV AH, 9
INT 21H
JMP EXIT

L2:
LEA DX, MSG2
MOV AH, 9
INT 21H
JMP EXIT

EXIT:
MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN