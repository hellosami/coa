.MODEL SMALL
.STACK 100H

.DATA
FIRST_TEXT DB 'INPUT FIRST NUMBER: $'
SECOND_TEXT DB 'INPUT SECOND NUMBER: $'

.CODE

MAIN PROC

MOV AX, @DATA
MOV DS, AX


MOV AH, 9 
LEA DX, FIRST_TEXT
INT 21H

MOV AH, 1
INT 21H
MOV BL, AL

; LINE BREAK
MOV AH, 2
MOV DL, 0AH
INT 21H

MOV DL, 0DH
INT 21H

MOV AH, 9 
LEA DX, SECOND_TEXT
INT 21H

MOV AH, 1
INT 21H
MOV BH, AL

; LINE BREAK
MOV AH, 2
MOV DL, 0AH
INT 21H

MOV DL, 0DH
INT 21H


MOV CX, 4D

L:

MOV DL, BL
MOV AH, 2
INT 21H


MOV DL, BH
MOV AH, 2
INT 21H

LOOP L





MAIN ENDP
END MAIN